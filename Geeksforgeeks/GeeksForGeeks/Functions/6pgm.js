//How to check whether a number is NaN or finite in JavaScript ?
 console.log(isNaN('Naga'))  //true

 console.log(isNaN('23')) //false

 console.log(isNaN(34)) //false

 /**
  * isFinite() = To determine whether the number is finite.
  */

 console.log(isFinite(34)) //true

 console.log(isFinite('23/03/2023')) //false

 console.log(isFinite('34')) //true
