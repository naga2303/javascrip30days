package Strings;

public class containsPgms
{
    public static void main(String args[])
    {
        String name = "Nagarajan";
        System.out.println(name.contains("za")); //false
        System.out.println(name.contains("ra")); //true
        System.out.println(name.contains("r")); //true
        System.out.println(name.contains("R")); //false
    }
}
